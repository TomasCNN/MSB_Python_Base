# Python中常用运算符
# 算术运算符
# | └─标准算术运算符:加(+)|减(-)|乘(*)|除(/)|整除(//)
# | └─取余运算符:%
# | └─幂运算符:**
# 赋值运算符
# 比较运算符
# 布尔运算符
# 位运算符


# 加法运算
print(1+1)
# 减法运算
print(1-1)
# 乘法运算
print(2*4)
# 除法运算
print(11/3)
# 整除运算
print(11//3)

# 取余运算(取模运算)
print(11%3)

# 幂运算
print(2**3)

# 负数(一正一负)取余运算(除数 = 被除数 - 除数 * 商)
print(9%-4)     # = 9 - (-4) * (-3) = -3

# 负数(一正一负)取整运算(一正一负向下取整)
print(9 // -4)  # = -(9 / 4 + 1) = -3
print(-9 // 4)  # = -(9 / 4 + 1) = -3

