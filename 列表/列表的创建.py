# 列表的创建
'''
    列表需要使用中括号[]。元素之间使用1英文的逗号进行分隔
    列表的创建方式
    1.使用中括号                  例如：list1 = [1,2,3,4,5]
    2.调用内置函数list（）         例如：list2 = list([1,2,3,4,5])
'''

# 创建列表的第一种方式，使用[]
list1 = [1,2,3,4,5]

print(id(list1[0]))
print(type(list1[0]))
print(list1[0])
print('\n')

print(id(list1[1]))
print(type(list1[1]))
print(list1[1])
print('\n')

print(id(list1[2]))
print(type(list1[2]))
print(list1[2])
print('\n')

print(id(list1[3]))
print(type(list1[3]))
print(list1[3])
print('\n')

print(id(list1[4]))
print(type(list1[4]))
print(list1[4])
print('\n')


